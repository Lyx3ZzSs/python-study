# \t制表符
print("jack\t20")

# \n换行
print("Hello,jack\nhello,tom")

# \\输出 \
print("D:\\python\\chapter02")

# \"输出"
print("杨过说：\"小龙女嫁给我吧\"")

# \'输出\'
print("郭靖说：\'hello\'")
# 单引号也可以不使用转义
print("郭靖说：'hello'")

# \r是回车(相当于掩盖前面的内容)
print("段誉向西行，\r他虽不会武功")
