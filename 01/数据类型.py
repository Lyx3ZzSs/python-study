# 整形 <class 'int'>
a = 1
print(type(a))

# 浮点型 <class 'float'>
b = 1.1
print(type(b))

# 布尔型 <class 'bool'>
c = True
print(type(c))

# 字符串 <class 'str'>
d = "123456"
print(type(d))

# 列表 <class 'list'>
e = [10, 20, 30]
print(type(e))

# 元组 <class 'tuple'>
f = (10, 20, 30)
print(type(f))

# 集合 <class 'set'>
h = {10, 20, 30}
print(type(h))

# 字典 <class 'dict'>
g = {"name": "zhangsan", "age": 18}
print(type(g))
